#include<iostream>

using namespace std;

int main()
{

    int GFG1, GFG2;

    GFG1 = 10;
    GFG2 = 3;



    cout << "GFG1 + GFG2 = " << (GFG1 + GFG2) << endl;

    cout << "GFG1 - GFG2 = " << (GFG1 - GFG2) << endl;

    cout << "GFG1 * GFG2 = " << (GFG1 * GFG2) << endl;

    cout << "GFG1 / GFG2 = " << (GFG1 / GFG2) << endl;

    cout << "GFG1 % GFG2 = " << (GFG1 % GFG2) << endl;


    return 0;

}
